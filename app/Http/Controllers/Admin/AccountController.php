     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $users = new User;

        $users->name = $request->name;
        $users->number = $request->number;
        $users->email = $request->email;
        $users->password = $request->password;

        $users->day = '19';
        $users->month = '01';
        $users->year = '01';
        $users->save();

        $rolesUser = new UserRole;
        $rolesUser->user_id = $users->id;
        $rolesUser->role_id = $request->role_id;
        $rolesUser->save();

        return redirect()
        ->route('account.index')
        ->with('Account','Bạn đã thêm sản phẩm thành công !!!');

    }

